def map_symptoms_to_dimensions(
    text: str, available_dims: Optional[set[str]] = None
) -> list[str]:
    """Map symptom text to dimension keys via keyword regex matching.

    Only returns dimensions the user actually has samples for
    (intersection with available_dims if provided).
    """
    if available_dims is None:
        available_dims = set(_MOCK_DIM_SAMPLES.keys())

    matched: list[str] = []
    seen: set[str] = set()
    for pattern, dims in SYMPTOM_DIM_MAP.items():
        if re.search(pattern, text):
            for d in dims:
                if d not in seen and d in available_dims:
                    matched.append(d)
                    seen.add(d)
    return matched


